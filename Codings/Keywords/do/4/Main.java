public class Main {
    public static void main(String[] args) {
        double radius = 5.0;

        double area = Math.PI * Math.pow(radius, 2);
        double sqrtValue = Math.sqrt(16.0);

        System.out.println("Area of the circle: " + area);
        System.out.println("Square root of 16: " + sqrtValue);
    }
}
