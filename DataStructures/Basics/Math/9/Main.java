public class Main {
    public static void main(String[] args) {
        int x = 10;
        int y = 8;

        // Minimum and Maximum
        int min = Math.min(x, y);
        int max = Math.max(x, y);

        System.out.println("Minimum: " + min);
        System.out.println("Maximum: " + max);
    }
}
