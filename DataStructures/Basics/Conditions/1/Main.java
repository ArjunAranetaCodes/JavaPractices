public class Main {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;

        // Addition
        int sum = a + b;
        System.out.println("Sum: " + sum);

        // Subtraction
        int difference = a - b;
        System.out.println("Difference: " + difference);

        // Multiplication
        int product = a * b;
        System.out.println("Product: " + product);

        // Division
        double quotient = (double) a / b;
        System.out.println("Quotient: " + quotient);
    }
}
