public class Main {
    public static void main(String[] args) {
        double x = 1000.0;
        double y = 64.0;
        
        double cubeRootX = Math.cbrt(x);
        double cubeRootY = Math.cbrt(y);
        
        System.out.println("Cube root of " + x + " is: " + cubeRootX);
        System.out.println("Cube root of " + y + " is: " + cubeRootY);
    }
}
   